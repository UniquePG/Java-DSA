public class Primitives {
    public static void main(String[] args) {
        // primitives datatype -> The type that we can not break even futher
        int a = 23;
        char latter = 'c';
        float num = 34.43f;
        boolean flag = true;
        double bigdata = 343434.54434534;
        long largeint = 39348394839L;   

        
        // String is not primitive datatype -> cause we can break string in to each charactor
        String name = "rishav";
        System.out.println("my name is " + name);
    }
}
